#include "../ft_parsing.h"

int		ft_strncmp(char *s1, char *s2, int x)
{
	int i;

	i = 0;
	while (s1[i] && s2[i] && i < x)
	{
		if (s1[i] != s2[i])
			return (0);
		i++;
	}
	return (1);
}
