int		ft_strcmp(char *s1, char *s2, int max)
{
	int i;

	i = 0;
	while ((s1[i] && s2[i]) || i < max)
	{
		if (s1[i] != s2[i])
			return (1);
		i++;
	}
	return (0);
}
